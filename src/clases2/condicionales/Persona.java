package condicionales;

public class Persona {

	String nombre;
	int edad;
	String ciudad;
	boolean estaBorracho;
	
	public Persona() {}

	Persona(String nombre, int edad, String ciudad, boolean estaBorracho) {
		this.nombre = nombre;
		this.edad = edad;
		this.ciudad = ciudad;
		this.estaBorracho = estaBorracho;
	}
}
